function localWriteJson(path, payload)
fid = fopen(path, 'w');
assert(fid > 0, 'Could not open fixture JSON for writing.');
cleanup = onCleanup(@() fclose(fid));
fprintf(fid, '%s', jsonencode(payload, 'PrettyPrint', true));
delete(cleanup);
end


function localRemoveDir(path)
