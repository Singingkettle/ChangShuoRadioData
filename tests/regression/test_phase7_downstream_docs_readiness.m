function test_phase7_downstream_docs_readiness()
%TEST_PHASE7_DOWNSTREAM_DOCS_READINESS Phase 7 release docs regression.

here = fileparts(mfilename('fullpath'));
projectRoot = fileparts(fileparts(here));
addpath(projectRoot);
addpath(fullfile(projectRoot, 'tools'));
addpath(fullfile(projectRoot, 'tools', 'release'));
addpath(fullfile(projectRoot, 'examples'));

docs = run_csrd_downstream_docs_readiness('Verbose', false);
assert(docs.Success, 'Phase 7 downstream docs readiness failed.');
assert(numel(docs.Documentation) >= 4, ...
    'Expected schema, downstream, release notes, and executable example checks.');
assert(all([docs.Documentation.Matched]), ...
    'All Phase 7 document content checks must pass.');

outDir = tempname;
mkdir(outDir);
cleanup = onCleanup(@() localRemoveDir(outDir));
annotationPath = fullfile(outDir, 'annotation_v2_fixture.json');
cocoPath = fullfile(outDir, 'coco_fixture.json');
localWriteJson(annotationPath, localFixturePayload());

summary = read_annotation_v2_downstream(annotationPath, cocoPath);

assert(strcmp(summary.Schema, 'annotation-v2'), ...
    'Downstream example must use annotation v2 reader summary.');
assert(summary.NumFrames == 1, 'Expected one fixture frame.');
assert(summary.NumSources == 1, 'Expected one fixture source.');
assert(summary.NumCocoAnnotations == 1, ...
    'Expected one visible COCO annotation.');
assert(strcmp(summary.CocoCoordinateSystem, 'receiver_frequency_canvas'), ...
    'COCO export must preserve receiver-frequency canvas semantics.');
assert(isfile(cocoPath), 'Downstream example did not write COCO JSON.');

decoded = jsondecode(fileread(cocoPath));
assert(decoded.csrd_export.num_annotations == 1, ...
    'Written COCO JSON annotation count drifted.');

delete(cleanup);
end


